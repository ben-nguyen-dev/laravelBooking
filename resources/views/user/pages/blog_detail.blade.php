							<h3 style="text-align: justify;"><strong><span id="Dich_vu_thue_xe_may">Dịch vụ thuê xe máy</span></strong><span class="ez-toc-section" id="Dich_vu_thue_xe_may"></span></h3>
							<p style="text-align: justify;"><strong>1. Dịch vụ cho thuê xe máy anh Cường</strong></p>
							<p style="text-align: justify;">Anh Cường có cửa hàng cho thuê xe máy tại sân bay và bến tàu Phú Quốc, ngoài ra anh cũng nhận giao xe miễn phí tận nơi tại bất kỳ đâu trên đảo Phú Quốc. Khi bạn nhận xe thì trong xe đã được chuẩn bị sẵn 1 lít xăng, bản đồ du lịch, 2 mũ bảo hiểm và áo mưa đi đường.<br>
							Bạn có thể đặt xe trước giờ bay đến Phú Quốc và khi tới nơi bạn được thoải mái chọn xe để thuê mà không cần phải đặt cọc bất kỳ thứ gì. Nếu&nbsp; xe máy bạn thuê gặp phải vấn đề gì trên đường đi, bạn chỉ cần liên hệ anh là sẽ được nhanh chóng hỗ trợ 24/24 tận nơi. Giá thuê xe số tại cửa hàng anh Cường là 120k/ngày, xe ga là 150k/ngày</p>
							<p style="text-align: justify;">Địa chỉ: Bến tàu Phú Quốc<br>
							Liên hệ: 0917774228 (a.Cường)</p>
							<p style="text-align: justify;"><strong>2. Dịch vụ thuê xe máy Cường Thịnh</strong></p>
							<p style="text-align: justify;">Anh Cường Thịnh cực kỳ nhiệt tình và vui tính, nên thuê xe của anh giá vừa rẻ mà dịch vụ lại tốt, khách thuê xe máy của anh đều hài lòng khi nhận hay trả xe. Anh cho thuê xe máy 24/24 với thủ tục nhanh chóng, giá thuê xe tay ga là 150k/ngày và xe số là 120k/ngày (thuê từ 2 ngày).<br>
							Đặc biệt bạn sẽ không cần phải đặt cọc, được giao xe miễn phí tại sân bay, bến tàu, khách sạn…hay bất kỳ đâu trên đảo Phú Quốc. Đồ dùng có sẵn khi nhận xe: 1 lít xăng, 2 nón bảo hiểm và áo mưa đi đường</p>
							<p style="text-align: justify;">Địa chỉ: Sân bay Phú Quốc<br>
							Liên hệ : 0981214344 (a.Thịnh)</p>
